package com.example.equipo.refugiosproyect.weather.model;

public class Wind {

    //ATRIBUTOS
    private double speed;
    private int deg;

    //CONSTRUCTOR
    public Wind() {
    }

    //GETTERS Y SETTERS
    public double getSpeed() {
        return speed;
    }

    public void setSpeed(double speed) {
        this.speed = speed;
    }

    public int getDeg() {
        return deg;
    }

    public void setDeg(int deg) {
        this.deg = deg;
    }
}
